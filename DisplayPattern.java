public class DisplayPattern {
  // Exercise 1.3 Display a pattern
  public static void main(String[] args) {
    System.out.println ("   J    A    V     V    A");
    System.out.println ("   J   A A    V   V    A A");
    System.out.println ("J  J  AAAAA    V V    AAAAA");
    System.out.println (" JJ  A     A    V    A     A");
    System.out.println (" ");
    System.out.println ("M     M     A     BBBB  U     U H   H     A    Y     Y");
    System.out.println ("MM   MM    A A    B   B U     U H   H    A A    Y   Y");
    System.out.println ("M MMM M   A   A   BBBB  U     U HHHHH   A   A    YYY");
    System.out.println ("M  M  M  AAAAAAA  B   B  U   U  H   H  AAAAAAA    Y");
    System.out.println ("M     M A       A BBBB    UUU   H   H A       A   Y");
  }
}